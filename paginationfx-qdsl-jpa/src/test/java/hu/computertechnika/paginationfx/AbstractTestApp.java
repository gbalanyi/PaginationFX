package hu.computertechnika.paginationfx;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.scene.layout.Pane;
import javafx.stage.Stage;

/**
 * @author GÃ¡bor Balanyi
 */
public abstract class AbstractTestApp extends Application {

	protected abstract String getFXML();

	public void start(Stage primaryStage) throws Exception {

		Pane parent = FXMLLoader.load(AbstractTestApp.class.getResource(this.getFXML()));
		Scene scene = new Scene(parent, 800, 480);
		primaryStage.setScene(scene);
		primaryStage.show();

	}

}
